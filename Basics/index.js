function getSum(a, b) {
  return a + b;
}
console.log(getSum(2, 4));

function calculator(first, second, opp) {
  if (opp === "+") {
    return first + second;
  } else if (opp === "-") {
    return first - second;
  } else if (opp === "*") {
    first * second;
  } else opp === "/";
  first / second;
}

console.log(calculator(6, 3, "-"));
